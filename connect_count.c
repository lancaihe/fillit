#include "libft.h"

char			*connect_count(char *str)
{
	int		i;
	int		c;

	i = 0;
	c = 0;
	while (str[i])
	{
		if (str[i] == '#')
		{
			if (str[i + 5] == '#')
				c++;
			if (str[i - 5] == '#')
				c++;
			if (str[i + 1] == '#')
				c++;
			if (str[i - 1] == '#')
				c++;
		}
		i++;
	}
	if (c != 6 && c != 8)
		throw_error();
	return (str);
}
