#include <string.h>

char		*ft_strncat(char *s1, char *s2, size_t n)
{
	size_t i;
	size_t last;

	i = 0;
	while (s1[i] != 0)
		i++;
	last = i;
	while (s2[i - last] != 0 && (i - last) < n)
	{
		s1[i] = s2[i - last];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
